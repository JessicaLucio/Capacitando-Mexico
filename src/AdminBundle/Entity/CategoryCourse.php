<?php

namespace AdminBundle\Entity;

/**
 * CategoryCourse
 */
class CategoryCourse
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;
    
    public function __toString() {
        return $this->getName();
    }


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return CategoryCourse
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }
}

